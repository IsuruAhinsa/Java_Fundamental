public class Variable13 {
    public static void main(String[] args) {
        int a = 10, b = 20;
        System.out.println('a'); // a
        System.out.println(a); // 10
        System.out.println("a"); // a
        System.out.println('b'); // b
        System.out.println(b); // 20
        System.out.println("b"); // b
    }
}
