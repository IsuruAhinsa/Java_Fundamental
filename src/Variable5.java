public class Variable5 {
    public static void main(String[] args) {
        int x;
        x = 100;
        System.out.println(x); // 100

        int y;
        y = 300;
        System.out.println(y); // 300

        System.out.println(x + y); // 400
        System.out.println("x + y"); // x + y
    }
}
