public class Day1 {
    public static void main(String[] args) {
        System.out.println("Welcome to Java"); // Welcome to Java
        System.out.println(2); // 2
        System.out.println("This is a Number:" + 2); // This is a Number:2
        System.out.println("This is a Number: " + 2); // This is a Number: 2
        System.out.println("This is a Number:" + " " + 2); // This is a Number: 2
        System.out.println("This is a Character:" + 'A'); // This is a Character:A
        System.out.println("This is a Character:" + "A"); // This is a Character:A
        System.out.println("This is a Character:" + "\u0052"); // This is a Character:R
        System.out.println("1007"); // 1007
        System.out.println(20+50); // 70
        System.out.println("20+50"); // 20+50
    }
}
